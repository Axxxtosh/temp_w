public class InternetPacksModel {
    String id;
    String Speed;
    String Data_transfer;
    String After_Fup;
    String Traiff;
    String GST;
    String Total;
    String Validity;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSpeed() {
        return Speed;
    }

    public void setSpeed(String speed) {
        Speed = speed;
    }

    public String getData_transfer() {
        return Data_transfer;
    }

    public void setData_transfer(String data_transfer) {
        Data_transfer = data_transfer;
    }

    public String getAfter_Fup() {
        return After_Fup;
    }

    public void setAfter_Fup(String after_Fup) {
        After_Fup = after_Fup;
    }

    public String getTraiff() {
        return Traiff;
    }

    public void setTraiff(String traiff) {
        Traiff = traiff;
    }

    public String getGST() {
        return GST;
    }

    public void setGST(String GST) {
        this.GST = GST;
    }

    public String getTotal() {
        return Total;
    }

    public void setTotal(String total) {
        Total = total;
    }

    public String getValidity() {
        return Validity;
    }

    public void setValidity(String validity) {
        Validity = validity;
    }
}
